/** 用户想查待提交/未完成作业（非单纯打开作业页） */
export function wantsAssignmentSummary(text) {
  const raw = (text || '').trim();
  if (!raw || raw.length > 120) return false;
  const t = raw.replace(/\s/g, '');

  if (/(?:打开|去|进入|跳转).{0,8}(?:作业页|作业中心|我的作业)/.test(t)) return false;

  const needles = [
    /什么.{0,10}作业.{0,8}(没|未|还没|没有).{0,6}交/,
    /哪些.{0,10}作业.{0,8}(没|未|还没|没有).{0,6}交/,
    /(没|未|还没|没有).{0,8}交.{0,10}作业/,
    /作业.{0,8}(没|未|还没|没有).{0,6}交/,
    /待交.{0,6}作业/,
    /待提交.{0,6}作业/,
    /还有什么.{0,8}作业/,
    /作业.{0,8}(进度|情况|咋样|怎样|如何)/,
    /我.{0,6}作业.{0,8}(交完|交齐|完成)/,
    /有没有.{0,8}作业.{0,6}要交/,
    /帮我.{0,6}查.{0,10}作业/,
    /^作业汇总$/,
  ];
  return needles.some((re) => re.test(t) || re.test(raw));
}
